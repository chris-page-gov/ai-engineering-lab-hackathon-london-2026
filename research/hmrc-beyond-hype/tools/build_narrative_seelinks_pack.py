def repo_relative(path: Path) -> str:
    return path.resolve().relative_to(REPO_ROOT).as_posix()


def slugify(value: str, limit: int = 72) -> str:
    slug = re.sub(r"[^a-z0-9]+", "-", value.lower()).strip("-")
    return slug[:limit].strip("-") or "item"


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8", errors="replace")


def strip_frontmatter(text: str) -> str:
    return re.sub(r"^---\n[\s\S]*?\n---\n+", "", text, count=1)


def parse_frontmatter_tags(text: str) -> list[str]:
    match = re.match(r"---\n(?P<body>[\s\S]*?)\n---\n", text)
    if not match:
        return []
    tags: list[str] = []
    in_tags = False
    for line in match.group("body").splitlines():
        if line.strip() == "tags:":
            in_tags = True
            continue
        if in_tags and line.startswith("  - "):
            tags.append(line.removeprefix("  - ").strip().strip('"'))
            continue
        if in_tags and line and not line.startswith(" "):
            break
    return sorted(set(tags))


def extract_heading(text: str, fallback: str) -> str:
    body = strip_frontmatter(text)
    match = re.search(r"^#\s+(.+?)\s*$", body, flags=re.MULTILINE)
    if match:
        return match.group(1).strip()
    return fallback


def extract_section(text: str, heading: str) -> str:
    body = strip_frontmatter(text)
    pattern = rf"^##\s+{re.escape(heading)}\s*$([\s\S]*?)(?=^##\s+|\Z)"
    match = re.search(pattern, body, flags=re.MULTILINE | re.IGNORECASE)
    if not match:
        return ""
    return clean_markdown(match.group(1))


def clean_markdown(text: str, limit: int | None = None) -> str:
    cleaned = re.sub(r"!\[[^\]]*\]\([^)]+\)", "", text)
    cleaned = re.sub(r"\[([^\]]+)\]\([^)]+\)", r"\1", cleaned)
    cleaned = re.sub(r"```[\s\S]*?```", "", cleaned)
    cleaned = re.sub(r"<!--[\s\S]*?-->", "", cleaned)
    cleaned = re.sub(r"^[-*]\s+", "", cleaned, flags=re.MULTILINE)
    cleaned = re.sub(r"\s+", " ", cleaned).strip()
    if limit and len(cleaned) > limit:
        return cleaned[: limit - 3].rstrip() + "..."
    return cleaned


def first_paragraph(text: str, fallback: str, limit: int = 700) -> str:
    body = strip_frontmatter(text)
    body = re.sub(r"^#\s+.+?$", "", body, count=1, flags=re.MULTILINE)
    paragraphs = [clean_markdown(part) for part in re.split(r"\n{2,}", body)]
    paragraphs = [
        part
        for part in paragraphs
        if part and not part.startswith("|") and not part.lower().startswith("## table of contents")
    ]
    return (paragraphs[0] if paragraphs else fallback)[:limit]


def word_count(text: str) -> int:
    return len(re.findall(r"\b[\w'-]+\b", strip_frontmatter(text)))


def estimate_screenfulls(text: str) -> int:
    return max(1, min(12, math.ceil(word_count(text) / 350)))


def infer_topic_groups(tags: list[str], text: str) -> list[str]:
    haystack = " ".join(tags).lower() + " " + text.lower()
    groups = [
        label
        for label, needles in TOPIC_GROUP_RULES
        if any(needle.lower() in haystack for needle in needles)
    ]
    return groups or ["Source material"]


def infer_governance_themes(tags: list[str], text: str) -> list[str]:
    haystack = " ".join(tags).lower() + " " + text.lower()
    themes = [
        label
        for label, needles in GOVERNANCE_THEME_RULES
        if any(needle.lower() in haystack for needle in needles)
    ]
    return themes[:5] or ["Source-backed answers"]


def infer_talk_section(source_id: str, item_number: int | None, tags: list[str], text: str) -> str:
    haystack = " ".join(tags).lower() + " " + text.lower()
    if source_id == "challenge-2-unlocking-dark-data":
        if item_number and item_number <= 3:
            return "Problem"
        return "Demo"
    if source_id == "dark-data-blueprint":
        return "Demo"
    if source_id == "governing-ai-engineering":
        return "Limits" if "risk" in haystack or "security" in haystack else "Try first"
    if source_id == "ai-native-engineering-blueprint":
        if item_number in {1, 2, 3, 4, 5}:
            return "Workflow"
        if item_number in {8, 9, 10, 11}:
            return "Strengths"
        return "Try first"
    if "question" in haystack or "q&a" in haystack:
        return "Q&A"
    if "security" in haystack or "risk" in haystack or "governance" in haystack:
        return "Limits"
    if "challenge 2" in haystack or "repo" in haystack or "workbench" in haystack:
        return "Repo"
    if "getting started" in haystack or "setup" in haystack or "try" in haystack:
        return "Try first"
    return "Workflow"


def infer_stage(source_id: str, item_number: int | None, tags: list[str], text: str) -> str:
    section = infer_talk_section(source_id, item_number, tags, text)
    if section == "Problem":
        return "Problem framing"
    if section == "Repo":
        return "Worked example"
    if section == "Demo":
        return "Provenance demo"
    if section == "Strengths":
        return "Contribution modes"
    if section == "Limits":
        return "Governance boundaries"
    if section == "Try first":
        return "Adoption path"
    if section == "Q&A":
        return "Question preparation"
    if "market-scan" in tags or "timeline" in tags:
        return "Market context"
    return "Workflow shift"


def infer_evidence_role(tags: list[str], text: str, asset_type: str) -> str:
    haystack = " ".join(tags).lower() + " " + text.lower()
    if asset_type in {"Slide", "PDF page", "Image"}:
        if "benchmark" in haystack or "evaluation" in haystack:
            return "Validation"
        if "risk" in haystack or "security" in haystack or "governance" in haystack:
            return "Control/risk"
        if "challenge-2" in tags or "workbench" in haystack:
            return "Demo object"
        return "Claim support"
    if "transcript" in haystack or "question" in haystack:
        return "Q&A prep"
    if "validation" in haystack or "report" in haystack:
        return "Validation"
    if "conversation" in haystack or "postmortem" in haystack:
        return "Source trail"
    if "getting started" in haystack or "setup" in haystack:
        return "Setup/start"
    return "Workflow pattern"


