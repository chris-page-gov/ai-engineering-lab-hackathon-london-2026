        tags = [tag for tag in tags if len(tag) <= 42][:18]
        full_summary = (
            extract_section(text, "Narrative Use")
            or extract_section(text, "Summary")
            or extract_section(text, "Key Talk Lines")
            or first_paragraph(text, title)
        )
        rel = repo_relative(doc.path)
        talk_section = doc.talk_section
        stage = doc.stage
