def property_defs(items: list[dict[str, Any]]) -> list[dict[str, Any]]:
    fields = [
        ("source_family", "Source Family", "multi", "Narrative"),
        ("narrative_stage", "Narrative Stage", "multi", "Narrative"),
        ("talk_section", "Talk Section", "multi", "Narrative"),
        ("asset_type", "Asset Type", "multi", "Source"),
        ("evidence_role", "Evidence Role", "multi", "Evidence"),
        ("governance_theme", "Governance Theme", "multi", "Governance"),
        ("topic_group", "Topic Group", "multi", "Narrative"),
        ("provenance_mode", "Provenance Mode", "multi", "Source"),
        ("screenfulls", "Screenfulls", "number", "Readability"),
        ("tags", "Tags", "multi", "Source"),
    ]
    defs: list[dict[str, Any]] = []
    for order, (field_id, name, kind, section) in enumerate(fields):
        values: set[str] = set()
        for item in items:
            value = item["properties"].get(field_id)
            if isinstance(value, list):
                values.update(str(entry) for entry in value if str(entry).strip())
            elif value not in (None, "") and kind != "number":
                values.add(str(value))
        entry: dict[str, Any] = {
            "id": field_id,
            "name": name,
            "kind": kind,
            "section": section,
            "order": order,
        }
        if values and len(values) <= 80:
            entry["values"] = sorted(values)
        defs.append(entry)
    return defs


def build_collections(items: list[dict[str, Any]]) -> list[dict[str, Any]]:
    by_section: dict[str, list[str]] = {section: [] for section in TALK_SECTIONS}
    for item in items:
        section = str(item["properties"].get("talk_section", "Q&A"))
        if section in by_section:
            by_section[section].append(item["id"])
    collections = []
    for section in TALK_SECTIONS:
        ids = by_section[section]
        if not ids:
            continue
        collections.append(
            {
                "id": f"talk-{slugify(section)}",
                "name": f"Talk path: {section}",
                "description": f"Cards that support the {section.lower()} section of the HMRC AI agents talk.",
                "item_ids": ids,
                "created_at": "2026-05-11T00:00:00Z",
            }
        )
    return collections


def build_graph(items: list[dict[str, Any]]) -> dict[str, Any]:
    nodes: dict[str, dict[str, Any]] = {}
    edges: list[dict[str, Any]] = []
    for item in items:
        item_id = item["id"]
        nodes[item_id] = {
            "id": item_id,
            "kind": "source",
            "title": item["name"],
            "summary": item["info_text"],
            "image": item.get("image") or item.get("icon"),
        }
        for key, kind in [
            ("source_family", "category"),
            ("narrative_stage", "category"),
            ("talk_section", "category"),
            ("evidence_role", "category"),
            ("topic_group", "tag"),
            ("governance_theme", "tag"),
        ]:
            raw = item["properties"].get(key)
            values = raw if isinstance(raw, list) else [raw]
            for value in values:
                if not value:
                    continue
                node_id = f"{key}:{slugify(str(value), 54)}"
                nodes.setdefault(
                    node_id,
                    {
                        "id": node_id,
                        "kind": kind,
                        "title": str(value),
                    },
                )
                edges.append(
                    {
                        "id": f"{item_id}->{node_id}",
                        "kind": "tagged_with" if kind == "tag" else "has_category",
                        "from": item_id,
                        "to": node_id,
                    }
                )
    return {"nodes": list(nodes.values()), "edges": edges, "sources": [], "claims": []}


def build_pack() -> dict[str, Any]:
    items = visual_items()
    items.extend(doc_items(len(items) + 1))
    return {
        "meta": {
            "id": "hmrc-narrative-arc",
            "title": "HMRC Beyond The Hype Narrative Arc",
            "source": "local",
            "version": "1.0.0",
            "packKind": "hmrc-narrative",
            "fields": [
                "source_family",
                "narrative_stage",
                "talk_section",
                "asset_type",
                "evidence_role",
                "governance_theme",
                "topic_group",
                "provenance_mode",
                "screenfulls",
                "tags",
            ],
        },
        "properties": property_defs(items),
        "items": items,
        "collections": build_collections(items),
        "graph": build_graph(items),
    }


def write_readme(pack: dict[str, Any]) -> None:
    source_counts: dict[str, int] = {}
    for item in pack["items"]:
        family = item["properties"]["source_family"]
        source_counts[family] = source_counts.get(family, 0) + 1
    rows = "\n".join(
        f"| {family} | {count} |" for family, count in sorted(source_counts.items())
    )
    README_PATH.write_text(
        f"""{GENERATED_MARKER}
# HMRC Narrative SeeLinks Datapack

This folder contains a generated SeeLinks-style datapack for the HMRC Beyond the Hype
AI coding assistants to agents talk narrative.

## Load Path

- Workbench URL: `challenge-2/workbench/?pack=hmrc-narrative`
- Pack JSON: `research/hmrc-beyond-hype/narrative/seelinks/pack.json`

## Coverage

- Items: {len(pack["items"])}
- Facets: {len(pack["properties"])}
- Collections: {len(pack["collections"])}
- Graph nodes: {len(pack["graph"]["nodes"])}
- Graph edges: {len(pack["graph"]["edges"])}

## Source Families

| Source family | Items |
| --- | ---: |
{rows}

## Facets

The primary facets are source family, narrative stage, talk section, asset type,
evidence role, governance theme, topic group, provenance mode, screenfulls, and tags.
They are intentionally broad enough to span slide thumbnails, narrative notes,
transcripts, repo evidence, and conversation traces while keeping the first-level
choices small enough for a live talk.
""",
        encoding="utf-8",
    )


def main() -> int:
    SEELINKS_DIR.mkdir(parents=True, exist_ok=True)
    pack = build_pack()
    PACK_PATH.write_text(json.dumps(pack, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    write_readme(pack)
    print(
        "Wrote HMRC narrative SeeLinks datapack: "
        f"{repo_relative(PACK_PATH)} ({len(pack['items'])} items)."
    )
    return 0


