def doc_items(start_index: int) -> list[dict[str, Any]]:
    items: list[dict[str, Any]] = []
    for offset, doc in enumerate(collect_source_docs(), start=start_index):
        text = read_text(doc.path)
        title = extract_heading(text, doc.path.stem.replace("-", " ").replace("_", " ").title())
