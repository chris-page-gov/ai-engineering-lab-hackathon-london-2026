def ascii_clean(value: str) -> str:
    replacements = {
        "\u2018": "'",
        "\u2019": "'",
        "\u201c": '"',
        "\u201d": '"',
        "\u2013": "-",
        "\u2014": "-",
        "\u2022": "-",
        "\u00a0": " ",
        "\u2122": "(TM)",
    }
    for old, new in replacements.items():
        value = value.replace(old, new)
    return value.encode("ascii", "ignore").decode("ascii")


def collapse_spaced_letters(value: str) -> str:
    pattern = re.compile(r"\b(?:[A-Za-z]\s+){2,}[A-Za-z]\b")
    return pattern.sub(lambda match: match.group(0).replace(" ", ""), value)


def normalise_text(value: str) -> str:
    value = ascii_clean(value)
    value = collapse_spaced_letters(value)
    value = value.replace("\r\n", "\n").replace("\r", "\n")
    lines = []
    for line in value.splitlines():
        line = re.sub(r"[ \t]+", " ", line).strip()
        if line:
            lines.append(line)
    return "\n".join(lines).strip()


def sentence_lines(value: str) -> list[str]:
    lines: list[str] = []
    for line in normalise_text(value).splitlines():
        line = re.sub(r"^\W+", "", line).strip(" -|")
        if len(line) >= 4 and not re.fullmatch(r"[0-9. ]+", line):
            lines.append(line)
    return lines


def material_points(value: str, fallback: str) -> list[str]:
    lines = sentence_lines(value)
    if not lines:
        return [fallback]
    points: list[str] = []
    seen: set[str] = set()
    for line in lines:
        alpha_count = sum(character.isalpha() for character in line)
        alnum_ratio = sum(character.isalnum() for character in line) / max(len(line), 1)
        if alpha_count < 3 or alnum_ratio < 0.45:
            continue
        key = line.lower()
        if key in seen:
            continue
        seen.add(key)
        if len(line) > 180:
            chunks = re.split(r"(?<=[.;:])\s+", line)
            points.extend(chunk for chunk in chunks if len(chunk) >= 4)
        else:
            points.append(line)
    return points or [fallback]


def title_from_text(default: str, item_label: str, text: str) -> str:
    ignored_prefixes = ("version:", "repository:", "branch:", "commit:", "$$")
    for line in sentence_lines(text):
        cleaned = re.sub(r"^[0-9]+[.)]?\s*", "", line)
        lower = cleaned.lower()
        word_count = len(re.findall(r"[A-Za-z]{3,}", cleaned))
        alnum_ratio = sum(character.isalnum() for character in cleaned) / max(len(cleaned), 1)
        if (
            8 <= len(cleaned) <= 90
            and word_count >= 2
            and alnum_ratio >= 0.55
            and not lower.startswith(ignored_prefixes)
        ):
            return cleaned
    return f"{default} - {item_label}"


