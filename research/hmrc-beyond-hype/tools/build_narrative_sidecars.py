def tesseract_path() -> str | None:
    candidate = shutil.which("tesseract")
    if candidate:
        return candidate
    fallback = Path("/opt/homebrew/bin/tesseract")
    if fallback.exists():
        return str(fallback)
    return None


def run_ocr(image: Image.Image) -> tuple[str, str]:
    executable = tesseract_path()
    if not executable:
        return "", "tesseract not available; OCR omitted"
    with tempfile.TemporaryDirectory() as tmp:
        image_path = Path(tmp) / "ocr-input.png"
        image.convert("RGB").save(image_path)
        result = subprocess.run(
            [executable, str(image_path), "stdout", "--psm", "6"],
            capture_output=True,
            text=True,
            check=False,
        )
    if result.returncode != 0:
        return "", normalise_text(result.stderr) or "tesseract OCR failed"
    return normalise_text(result.stdout), ""


def iter_picture_shapes(shapes):
    for shape in shapes:
        if shape.shape_type == MSO_SHAPE_TYPE.PICTURE:
            yield shape
        elif shape.shape_type == MSO_SHAPE_TYPE.GROUP:
            yield from iter_picture_shapes(shape.shapes)


def render_pdf_page(source_path: Path, page_number: int) -> Image.Image:
    executable = shutil.which("pdftoppm")
    if not executable and Path("/opt/homebrew/bin/pdftoppm").exists():
        executable = "/opt/homebrew/bin/pdftoppm"
    if not executable:
        raise RuntimeError("pdftoppm is required to render PDF page thumbnails")
    with tempfile.TemporaryDirectory() as tmp:
        prefix = Path(tmp) / "page"
        result = subprocess.run(
            [
                executable,
                "-jpeg",
                "-r",
                "144",
                "-f",
                str(page_number),
                "-l",
                str(page_number),
                "-singlefile",
                str(source_path),
                str(prefix),
            ],
            capture_output=True,
            text=True,
            check=False,
        )
        if result.returncode != 0:
            raise RuntimeError(result.stderr.strip() or "pdftoppm failed")
        rendered = prefix.with_suffix(".jpg")
        with Image.open(rendered) as image:
            return image.copy()


