    ensure_dirs()
    remove_generated_tree(SLIDES_DIR)
    remove_generated_tree(ASSETS_DIR)
    rows: list[dict[str, str]] = []
    for source in VISUAL_SOURCES:
        if source.kind == "pptx":
            built = build_pptx(source)
        elif source.kind == "pdf":
            built = build_pdf(source)
        elif source.kind == "png":
            built = build_png(source)
        else:
            raise ValueError(f"Unhandled source kind: {source.kind}")
        rows.extend(built)
        print(f"{source.filename}: {len(built)} item(s) covered")

    build_clawpilot_note()
    build_ai_coding_assistants_note()
    build_audio_source_notes()
    write_import_inventory()
    write_core_narrative(rows)
    print(f"Total visual sidecars covered: {len(rows)}")
    return 0


